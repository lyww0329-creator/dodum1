    function openAddressSearch() {
      const addressInput = document.getElementById('addressInput');
      const detailAddress = document.getElementById('detailAddress');

      if (!addressInput) return;

      if (!window.daum || !window.daum.Postcode) {
        alert('주소 검색 스크립트를 불러오지 못했습니다. 인터넷 연결을 확인한 뒤 다시 시도해 주세요.');
        addressInput.focus();
        return;
      }

      new window.daum.Postcode({
        oncomplete: function(data) {
          const roadAddress = data.roadAddress || '';
          const jibunAddress = data.jibunAddress || '';
          const extraRoadAddrParts = [];

          if (data.bname && /[동|로|가]$/g.test(data.bname)) {
            extraRoadAddrParts.push(data.bname);
          }
          if (data.buildingName && data.apartment === 'Y') {
            extraRoadAddrParts.push(data.buildingName);
          }

          const extraRoadAddr = extraRoadAddrParts.length ? ' (' + extraRoadAddrParts.join(', ') + ')' : '';
          addressInput.value = (roadAddress || jibunAddress) + extraRoadAddr;

          if (detailAddress) {
            detailAddress.focus();
          }
        }
      }).open();
    }

    function updateGradeDetailSelect(stage) {
      const detailSelect = document.getElementById('gradeDetailSelect');
      if (!detailSelect) return;

      const detailMap = {
        '초등': ['초1', '초2', '초3', '초4', '초5', '초6'],
        '중등': ['중1', '중2', '중3'],
        '고등': ['고1', '고2', '고3'],
        '성인/N수생': ['성인', 'N수생'],
        '성인': ['성인']
      };

      const options = detailMap[stage] || [];
      detailSelect.innerHTML = options.length
        ? '<option value="">세부 학년 선택</option>' + options.map(item => '<option value="' + item + '">' + item + '</option>').join('')
        : '<option value="">먼저 학교급을 선택해주세요</option>';

      detailSelect.disabled = !options.length;
      detailSelect.required = !!options.length;
    }

    function handleContactSubmit(event) {
      event.preventDefault();
      const form = event.currentTarget;
      if (!form.checkValidity()) {
        form.reportValidity();
        return;
      }
      if (NAVER_FORM_URL) {
        var formWindow = window.open(NAVER_FORM_URL, '_blank');
        if (formWindow) formWindow.opener = null;
        return;
      }
      openSubmitSuccessModal();
      form.reset();
      updateGradeDetailSelect('');
    }

