    function render(route = 'home') {
      const [page, a, b, c, d] = route.split('/').map(decodeURIComponent);
      const languageTheme = page === 'language' ? (a || '영어') : (page === 'languages' ? '영어' : '');
      document.body.classList.toggle('language-theme-active', !!languageTheme);
      if (languageTheme) document.body.dataset.languageTheme = languageTheme;
      else delete document.body.dataset.languageTheme;
      if (page === 'subject') {
        const subjectParts = route.split('/').map(decodeURIComponent);
        const subjectName = subjectParts[1] || '수학';
        const tokens = subjectParts.slice(2).filter(Boolean);
        let subjectGrade = '';
        let subjectProvince = '';
        let subjectDistrict = '';
        let subjectTown = '';
        for (let i = 0; i < tokens.length; i++) {
          const token = tokens[i];
          if (token === '__grade__') {
            subjectGrade = tokens[i + 1] || '';
            i++;
          } else if (token === '__province__') {
            subjectProvince = tokens[i + 1] || '';
            i++;
          } else if (token === '__district__') {
            subjectDistrict = tokens[i + 1] || '';
            i++;
          } else if (token === '__town__') {
            subjectTown = tokens[i + 1] || '';
            i++;
          } else if (GRADE_GROUPS[token]) {
            subjectGrade = token;
          }
        }
        app.innerHTML = subjectsView(subjectName, subjectGrade, subjectProvince, subjectDistrict, subjectTown);
      }
      else if (page === 'subjects') app.innerHTML = subjectsView('수학');
      else if (page === 'region') {
        const parsedRegion = parseRegionRoute(route);
        if (parsedRegion.regionTown) {
          app.innerHTML = regionView(
            parsedRegion.provinceName,
            parsedRegion.regionCity,
            parsedRegion.regionDistrict,
            parsedRegion.regionTown,
            parsedRegion.regionSubject,
            parsedRegion.regionGrade
          );
        } else {
          const rawParts = route.split('/').map(decodeURIComponent);
          const hasProvince = rawParts.length > 1 && REGION_TREE[rawParts[1]];
          app.innerHTML = regionPickerView(
            hasProvince ? parsedRegion.provinceName : '',
            hasProvince ? parsedRegion.regionCity : '',
            hasProvince ? parsedRegion.regionDistrict : '',
            parsedRegion.regionSubject,
            parsedRegion.regionGrade
          );
        }
      }
      else if (page === 'grade') {
        const gradeParts = route.split('/').map(decodeURIComponent);
        const groupName = gradeParts[1] || '초등';
        const tokens = gradeParts.slice(2);
        let detailGrade = '';
        let gradeSubject = '';
        let gradeProvince = '';
        let gradeDistrict = '';
        let gradeTown = '';

        for (let i = 0; i < tokens.length; i++) {
          const token = tokens[i] || '';
          if (!token) continue;
          if (token === '__subject__') {
            gradeSubject = tokens[i + 1] || '';
            i++;
          } else if (token === '__province__') {
            gradeProvince = tokens[i + 1] || '';
            i++;
          } else if (token === '__district__') {
            gradeDistrict = tokens[i + 1] || '';
            i++;
          } else if (token === '__town__') {
            gradeTown = tokens[i + 1] || '';
            i++;
          } else if (!detailGrade && (GRADE_GROUPS[groupName] || []).includes(token)) {
            detailGrade = token;
          } else if (!gradeSubject && SUBJECTS[token]) {
            gradeSubject = token;
          }
        }

        app.innerHTML = gradeView(groupName, detailGrade, gradeSubject, gradeProvince, gradeDistrict, gradeTown);
      }
      else if (page === 'language') app.innerHTML = languagesView(a || '영어');
      else if (page === 'languages') app.innerHTML = languagesView('영어');
      else if (page === 'contact') app.innerHTML = contactView(parseContactPrefill(route));
      else app.innerHTML = homeView();
      updateMobileActive(page);
      scrollTop();
      startReviewCarousel();
    }

