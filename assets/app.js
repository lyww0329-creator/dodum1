    function buildRegionRoute(province, district = '', town = '', grade = '', subject = '') {
      const provinceName = province || '서울';
      if (isDirectTownProvince(provinceName)) {
        return buildRegionDeepRoute(provinceName, '', '', town || district, grade, subject);
      }
      if (isMetroProvince(provinceName)) {
        return buildRegionDeepRoute(provinceName, '', district, town, grade, subject);
      }
      const cityName = district || '';
      const guName = findRegionDistrictByTown(provinceName, cityName, town || '');
      return buildRegionDeepRoute(provinceName, cityName, guName, town || '', grade, subject);
    }

    function parseRegionRoute(route = '') {
      const regionParts = route.split('/').map(decodeURIComponent);
      const provinceName = REGION_TREE[regionParts[1]] ? regionParts[1] : '서울';
      const tokens = regionParts.slice(2).filter(Boolean);
      let regionCity = '';
      let regionDistrict = '';
      let regionTown = '';
      let regionSubject = '';
      let regionGrade = '';
      const locationTokens = [];

      for (let i = 0; i < tokens.length; i++) {
        const token = tokens[i];
        if (token === '__subject__') {
          regionSubject = tokens[i + 1] || '';
          i++;
        } else if (token === '__grade__') {
          regionGrade = tokens[i + 1] || '';
          i++;
        } else {
          locationTokens.push(token);
        }
      }

      const provinceMap = REGION_TREE[provinceName] || {};

      if (isDirectTownProvince(provinceName)) {
        const townList = getRegionTownList(provinceName);
        if (locationTokens[0] && townList.includes(locationTokens[0])) regionTown = locationTokens[0];
        return { provinceName, regionCity, regionDistrict, regionTown, regionSubject, regionGrade };
      }

      if (isMetroProvince(provinceName)) {
        if (locationTokens[0] && provinceMap[locationTokens[0]]) regionDistrict = locationTokens[0];
        const townList = regionDistrict ? getRegionTownList(provinceName, '', regionDistrict) : [];
        if (locationTokens[1] && townList.includes(locationTokens[1])) regionTown = locationTokens[1];
        return { provinceName, regionCity, regionDistrict, regionTown, regionSubject, regionGrade };
      }

      if (locationTokens[0] && provinceMap[locationTokens[0]]) {
        regionCity = locationTokens[0];
      }

      const cityDistrictMap = getCityDistrictMap(provinceName, regionCity);
      const cityTownList = regionCity ? (provinceMap[regionCity] || []) : [];

      if (cityDistrictMap) {
        if (locationTokens[1] && cityDistrictMap[locationTokens[1]]) {
          regionDistrict = locationTokens[1];
          const townList = cityDistrictMap[regionDistrict] || [];
          if (locationTokens[2] && townList.includes(locationTokens[2])) regionTown = locationTokens[2];
        } else if (locationTokens[1] && cityTownList.includes(locationTokens[1])) {
          regionTown = locationTokens[1];
          regionDistrict = findRegionDistrictByTown(provinceName, regionCity, regionTown);
        }
      } else if (locationTokens[1] && cityTownList.includes(locationTokens[1])) {
        regionTown = locationTokens[1];
      }

      return { provinceName, regionCity, regionDistrict, regionTown, regionSubject, regionGrade };
    }

